import mongoose from "mongoose"

const OrderSchema = mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
    orderItems: [
      {
        name: { type: String, required: true },
        qty: { type: Number, required: true },
        image: { type: String, required: true },
        product: {
          type: mongoose.Schema.Types.ObjectId,
          required: true,
          ref: "Product",
        },
      },
    ],
    shippingAddress: {
      address: { type: String},
      city: { type: String},
      postalCode: { type: String },
      country: { type: String},
      borrowingDate: { type: Date, required: true },
      returnDate: { type: Date, required: true },
      reason: { type: String, required: true } 
  },
  
    itemsPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    taxPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    shippingPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    totalPrice: {
      type: Number,
      required: true,
      default: 0.0,
    },
    paidAt: {
      type: Date,
    },
    isDelivered: {
      type: Boolean,
      required: true,
      default: false,
    },
    deliveredAt: {
      type: Date,
    },
    status: {
      type: String,
      enum: ["Confirm", "Pending", "Cancel"], 
      default: "Pending", 
    },
  },
  {
    timestamps: true,
  }
)

const Order = mongoose.model("Order", OrderSchema)

export default Order
